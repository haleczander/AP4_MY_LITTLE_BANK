class Account:
    def __init__(self):
        pass

    def set_id(self, id):
        self.id = id

    def set_sold(self, sold):
        self.sold = sold

    def set_currency(self, currency):
        self.currency = currency

    def get_id(self):
        return self.id

    def get_sold(self):
        return self.sold

    def get_currency(self):
        return self.currency
