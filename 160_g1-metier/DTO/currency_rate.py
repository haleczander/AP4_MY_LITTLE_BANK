class CurrencyRate:
    def __init__(self, currency_a, currency_b, rate):
        self.currency = currency_a
        self.currency = currency_b
        self.rate = rate
    def get_currency_a(self):
        return self.currency_a
    def get_currency_b(self):
        return self.currency_b
    def get_rate(self):
        return self.rate
